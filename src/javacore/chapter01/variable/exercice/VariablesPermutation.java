package javacore.chapter01.variable.exercice;

public class VariablesPermutation {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a);
        System.out.println(b);
        int c;
        c = a;
        a = b;
        b = c;
        System.out.println(a);
        System.out.println(b);

    }
}
